#include "hmi_user_uart.h"
#include "main.h"


/*****************************************************************
* 名    称： SendChar()
* 功    能： 发送1个字节
* 入口参数： t  发送的字节
* 出口参数： 无
 *****************************************************************/
void  SendChar(uchar t)
{
		 usart_data_transmit(USART2, t);
	  while(RESET == usart_flag_get(USART2, USART_FLAG_TBE));
}
