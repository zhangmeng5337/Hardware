#include "stm8s.h"
#include "gpio.h"


void MCU_gpio_init(void)
{
 GPIO_Init(HW3000_CSN_PORT, HW3000_CSN_IO,GPIO_MODE_OUT_PP_HIGH_FAST);
 GPIO_Init(HW3000_CLK_PORT, HW3000_CLK_IO,GPIO_MODE_OUT_PP_LOW_FAST);
 GPIO_Init(HW3000_SDO_PORT, HW3000_SDO_IO,GPIO_MODE_OUT_PP_HIGH_FAST);
 GPIO_Init(HW3000_SDI_PORT, HW3000_SDI_IO,GPIO_MODE_IN_PU_NO_IT);
 GPIO_Init(HW3000_GPIO0_PORT, HW3000_GPIO0_IO,GPIO_MODE_IN_PU_IT);
 GPIO_Init(HW3000_GPIO1_PORT, HW3000_GPIO1_IO,GPIO_MODE_IN_PU_IT);
 GPIO_Init(HW3000_GPIO2_PORT, HW3000_GPIO2_IO,GPIO_MODE_IN_PU_IT);
 GPIO_Init(HW3000_IRQ_PORT, HW3000_IRQ_IO,GPIO_MODE_IN_PU_IT);
 GPIO_Init(HW3000_PDN_PORT, HW3000_PDN_IO,GPIO_MODE_OUT_PP_LOW_FAST);
 
 GPIO_Init(HW3000_KEY1_PORT, HW3000_KEY1_IO,GPIO_MODE_IN_PU_NO_IT);
 GPIO_Init(HW3000_KEY2_PORT, HW3000_KEY2_IO,GPIO_MODE_IN_PU_NO_IT);
 GPIO_Init(HW3000_KEY3_PORT, HW3000_KEY3_IO,GPIO_MODE_IN_PU_NO_IT);
 
 GPIO_Init(RF_LED_PORT, RF_LED_IO,GPIO_MODE_OUT_PP_HIGH_FAST);
 GPIO_Init(RF_LED_PORT1, RF_LED_IO1,GPIO_MODE_OUT_PP_HIGH_FAST);
 
}


