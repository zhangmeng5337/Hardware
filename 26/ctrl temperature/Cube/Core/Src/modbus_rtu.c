 //crc校验
 uint16_t CRC16 (uint8_t arr_buff[], uint8_t len)
{
	register int i = 0,j = 0;
	register uint16_t crc16 = 0xFFFF;
	
	uint16_t data_CRC[len-2];
    
	for(i = 0; i < len-2; i ++)
	{
		data_CRC[i] = arr_buff[i];
	}
	
	for (i = 0;i < len-2;i++)
	{
		crc16 = crc16 ^ data_CRC[i];
		for (j = 0;j < 8;j++)
		{

			if (crc16 & 0x01)
			{
				crc16 = (crc16 >> 1) ^ 0xa001;}
			else 
			{
				crc16 = crc16 >> 1;}
			}
	}
	
	return crc16;
}



//协议通信 crc校验和
//* @brief calsum 计算校验和
//* @param data 数据
//* @param nLen 长度
//* @return 校验和
//*/
char calsum(const char *data, int nLen)
{
char sum = 0x00;
for(int i = 0; i < nLen; i++)
{
sum += *data++;
}
sum = ~sum + 1;
return sum;
}

