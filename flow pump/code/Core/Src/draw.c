// 显示128*64点阵图像
// 从基本指令集切换到扩展指令集，一定要先清屏：LCD_Clear_GDRAM();否则会重叠显示
// 从扩展指令集切换到基本指令集，一定要先1.LCD_WriteCMD(0x30);  //绘图显示关，基本指令集
//2.delay_us(370); //delay is important!;否则字符不会显示出来
void LCD_Disp_Image(const u8 *str)
{
    u16 i, j;
    LCD_WriteCMD(0x30);      //8Bit扩充指令集,即使是36H也要写两次
    LCD_WriteCMD(
        0x36);  //绘图显示开，扩充指令集extended instruction(DL=8BITS,RE=1,G=1)
    delay_us(370);         //delay is important!

//*******显示上半屏内容
    for (i = 0; i < 32; i++)       //20H
    {
        LCD_WriteCMD(0x80 + i);   //SET  垂直地址 VERTICAL ADD
        LCD_WriteCMD(0x80);       //SET  水平地址 HORIZONTAL ADD
        for (j = 0; j < 16; j++)
        {
            LCD_WriteDATA(*str++);
        }
    }

//*******显示下半屏内容
    for (i = 0; i < 32; i++)
    {
        LCD_WriteCMD(0x80 + i); //SET 垂直地址 VERTICAL ADD
        LCD_WriteCMD(0x88);     //SET 水平地址 HORIZONTAL ADD
        for (j = 0; j < 16; j++)
        {
            LCD_WriteDATA(*str++);
        }
    }

}

