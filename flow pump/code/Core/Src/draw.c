// col+1, 水平移动16dot
void LCD_Disp_Large_Num2432(const u8 *Font, u8 col, u8 Num)
{
    u16 i, j;
    LCD_WriteCMD(
        0x36);  //绘图显示开，扩充指令集extended instruction(DL=8BITS,RE=1,G=1)
    delay_us(370);        //delay is important!

    for (i = 0; i < 32; i++)       //20H
    {
        LCD_WriteCMD(0x80 + i);   //SET  垂直地址 VERTICAL ADD //往下移动
        LCD_WriteCMD(0x80 + col);  //SET 水平地址 HORIZONTAL ADD,防止自动加1
        for (j = 0; j < 3; j++)
        {
            LCD_WriteDATA(*(Num * 96 + Font++));
        }
    }
}

// col+1, 水平移动16dot
void LCD_Disp_Large_Num1632(const u8 *Font, u8 col, u8 Num, bool reverse)
{
    u16 i, j;
    LCD_WriteCMD(
        0x36);  //绘图显示开，扩充指令集extended instruction(DL=8BITS,RE=1,G=1)
    delay_us(370);        //delay is important!

    for (i = 0; i < 32; i++)       //20H
    {
        LCD_WriteCMD(0x80 + i);   //SET  垂直地址 VERTICAL ADD //往下移动
        LCD_WriteCMD(0x80 + col);  //SET 水平地址 HORIZONTAL ADD,防止自动加1
        for (j = 0; j < 2; j++)
        {
            if (reverse)
                LCD_WriteDATA(~ * (Num * 64 + Font++));
            else
            {
                LCD_WriteDATA(*(Num * 64 + Font++));
            }
        }
    }
}


