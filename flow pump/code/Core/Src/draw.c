/***********************************************************
row:1-4
x_pos:0-7
y_pos:1-16
return:y addr,x addr
************************************************************/
unsigned int posXY;
unsigned int lcd_pos_extcmd(unsigned char row, unsigned char x_pos,
                            unsigned char y_pos)
{
    
    posXY = 0;
    switch (row)
    {
        case 1:
            posXY = 0x80; //y
            posXY = posXY + y_pos;
            posXY = posXY - 1;
            posXY = posXY << 8;
            posXY = posXY | 0x80; //x
            posXY = posXY + x_pos;//
            posXY = posXY - 1;
            break;
        case 2:
            posXY = posXY + 0x90; //y
            posXY = posXY + y_pos;
            posXY = posXY - 1;
            posXY = posXY << 8;
            posXY = posXY | 0x80; //x
            posXY = posXY + x_pos;//
            posXY = posXY - 1;

            break;
        case 3:
            posXY = posXY + 0x80; //y
            posXY = posXY + y_pos;
            posXY = posXY - 1;
            posXY = posXY << 8;
            posXY = posXY | 0x88; //x
            posXY = posXY + x_pos;//
            posXY = posXY - 1;

            break;
        case 4:
            posXY = posXY + 0x90; //y
            posXY = posXY + y_pos;
            posXY = posXY - 1;
            posXY = posXY << 8;
            posXY = posXY | 0x88; //x
            posXY = posXY + x_pos;//
            posXY = posXY - 1;

            break;


        default:
            posXY = 0x8080;
            break;

    }
    return posXY;
}


void LCD_Row_highlight(unsigned char row, unsigned char x_pos,
                       unsigned char y_pos,
                       unsigned char row_size, unsigned char col_size)
{
    unsigned char X = 0x80, Y = 0x80;
    unsigned char i, j;
    unsigned int result;

    result = lcd_pos_extcmd(row, x_pos, y_pos);
    Y = result >> 8;
    X = result;

    for (i = 0; i < row_size; i++)
    {
        for (j = 0; j < col_size; j++)
        {
            LCD_WriteCMD(0x34);
            LCD_WriteCMD(0x34);
            LCD_WriteCMD(Y);
            LCD_WriteCMD(X);
            LCD_WriteDATA(0xff);      //  low 8 bits
            LCD_WriteDATA(0xff);      //  low 8 bits

            Y++;
            j++;
        }

    }
}
unsigned char lcd_fresh_enable(unsigned char enbale)
{
    unsigned result;
    result = 0;
    if (enbale)
    {
        registerTick(LCD_BLINK_TICK, LCD_BLINK_TIME);

            if (GetTickResult(LCD_BLINK_TICK) == 1)
            {
                result = 1;
                reset_registerTick(LCD_BLINK_TICK);
                registerTick(LCD_BLINK_TICK, LCD_BLINK_TIME);
            }
    }
    return result;

}

