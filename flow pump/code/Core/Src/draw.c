//ST7920内嵌了4个16×16的软件可编程的CGRAM
//能存储4个用户自定义字符的字模（16×16点阵），大小1K位。
//字符编码使用两个字节表示,分别为：0000h,0002h,0004h,0006h
//自定义字符写入CGRAM,不能断电保存
void LCD_Write_USR_CGRAM(void)
{
    //前三个user char的首地址0x40,0x50,0x60,0x70
    for (u8 j = 0; j < 4; j++)
    {
        LCD_WriteCMD(0x40 + j * 16);
        for (u8 i = 0; i < 16; i++)    //心形
        {
            LCD_WriteDATA(usr_char[i * 2 + j * 32]);
            LCD_WriteDATA(usr_char[i * 2 + 1 + j * 32]);
        }

    }

}



/********************************************************************
* 名称 : LCD_DISP_USR_Char
* 功能 : 在当前坐标位置显示单个字符
* 输入 : 显示的位置,cgram 中字符索引 index=1,2,3,4
* 说明: //在使用该函数前，确保已执行LCD_Write_USR_CGRAM()
//显示自定义的字符,并把这个字符填满16*16
***********************************************************************/

void LCD_DISP_USR_Char(u8 row, u8 col, u8 index)
{

    LCD_Set_XY(row, col);   //cgram char 映射输出的DDRAM地址
    LCD_WriteDATA(0x00);  //Must exist!
    LCD_WriteDATA((index - 1) *
                  2); // cgram :00,02,04,06  第index个字符存储地址偏移量

}


